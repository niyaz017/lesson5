void main() {
  // 1) Если переменная a равна 10, то выведите 'Верно', иначе выведите
  // 'Неверно'

  int a = 10;

  if (a == 10) {
    print('Верно');
  } else {
    print('Неверно');
  }

  // var num = a == 0 ? 'Верно' : 'Неверно';

  //2) Если переменная a равна нулю, то выведите 'Верно' иначе выведите
