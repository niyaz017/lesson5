  int a1 = 0;

  if (a1 == 0) {
    print('верно');
  } else {
    print('неверно');
  }

  // 3)  Если переменная a больше нуля, то выведите 'Верно'
  //иначе выведите 'Неверно' Проверьте работу скрипта при a, равном 1, 0, -3.

  int a2 = 1;

  if (a2 > 0) {
    print('True');
  } else {
    print('False');
  }

  // // 4)  Если переменная a меньше нуля, то выведите 'Верно', иначе выведите
  // //'Неверно'. Проверьте работу скрипта при a, равном 1, 0, -3.

  int a3 = -3;

  if (a3 < 0) {
    print('true');
  } else {
    print('false');
  }

  // //5)  Если переменная a больше или равна нулю, то выведите 'Верно', иначе
